def project_to_image(pts_3d, P):
    ''' Project 3d points to image plane.

    Usage: pts_2d = projectToImage(pts_3d, P)
      input: pts_3d: nx3 matrix
             P:      3x4 projection matrix
      output: pts_2d: nx2 matrix

      P(3x4) dot pts_3d_extended(4xn) = projected_pts_2d(3xn)
      => normalize projected_pts_2d(2xn)

      <=> pts_3d_extended(nx4) dot P'(4x3) = projected_pts_2d(nx3)
          => normalize projected_pts_2d(nx2)
    '''
    n = pts_3d.shape[0]
    pts_3d_extend = np.hstack((pts_3d, np.ones((n,1))))
    pts_2d = np.dot(pts_3d_extend, np.transpose(P)) # nx3
    pts_2d[:,0] /= pts_2d[:,2]
    pts_2d[:,1] /= pts_2d[:,2]
    return pts_2d[:,0:2]

def tf_project_to_image_tensor(pts_3d, P):
    """Projects 3D points to 2D points in image space.

    Args:
        points_3d: a list of float32 tensor of shape [-1, 3]
        cam_p2_matrix: a float32 tensor of shape [3, 4] representing
            the camera matrix.

    Returns:
        points_2d: a list of float32 tensor of shape [-1, 2]
            This is the projected 3D points into 2D .i.e. corresponding
            3D points in image coordinates.
    """
    n = tf.shape(pts_3d)[0]
    pts_3d_extend = tf.concat([pts_3d, tf.ones([n, 1], dtype=pts_3d.dtype)], axis=-1) 
    pts_2d = tf.matmul(pts_3d_extend, tf.transpose(P)) # nx3
    pts_2d_x = pts_2d[:, 0] / pts_2d[:, 2]
    pts_2d_y = pts_2d[:, 1] / pts_2d[:, 2]
    pts_2d = tf.stack([pts_2d_x, pts_2d_y], axis=-1) # nx2

    return pts_2d 
